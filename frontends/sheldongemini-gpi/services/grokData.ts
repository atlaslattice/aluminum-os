export interface SphereCard {
    index: number;
    sphere: string;
    element: string;
    god: string;
    mythical: string;
    numerology: Record<string, string>;
    description: string;
}

export const getSphereCard = (index: number): SphereCard | null => {
    if (index < 0 || index >= SPHERES.length) return null;

    const sphere = SPHERES[index];
    const element = ELEMENTS[index];
    const god = GODS[index];
    const mythical = MYTHICAL_OVERLAYS[index];
    
    const numOverlays = {
        tibetan: NUMEROLOGY.tibetan(index),
        kabbalah: NUMEROLOGY.kabbalah(index),
        iching: NUMEROLOGY.iching(index),
        christian: NUMEROLOGY.christian(index, sphere),
        sikh: NUMEROLOGY.sikh(index),
        native: NUMEROLOGY.native(index)
    };

    const desc = `Sphere #${index + 1}: ${sphere}\n` +
                 `- Element: ${element}\n` +
                 `- God/Archetype: ${god}\n` +
                 `- Mythical Overlay: ${mythical}\n` +
                 `- Numerology: ${JSON.stringify(numOverlays)}`;

    return {
        index,
        sphere,
        element,
        god,
        mythical,
        numerology: numOverlays,
        description: desc
    };
};

// Search function for the static knowledge base
export const searchGrokKnowledge = (query: string): string => {
    const q = query.toLowerCase();
    const matches: string[] = [];

    // 1. PROJECT SEARCH (High Priority)
    for (const [project, keywords] of Object.entries(PROJECT_KEYWORDS)) {
        // If the query contains any of the project keywords
        if (keywords.some(kw => q.includes(kw.toLowerCase()))) {
            matches.push(`ACTIVE PROJECT DETECTED: [${project.toUpperCase()}]\nKeywords: ${keywords.join(', ')}\nSheldon, recall your involvement in this project.`);
        }
    }

    // 2. Check for specific numbers (e.g., "Sphere 144")
    const numMatch = q.match(/sphere\s?(\d+)/);
    if (numMatch && numMatch[1]) {
        const idx = parseInt(numMatch[1], 10) - 1;
        const card = getSphereCard(idx);
        if (card) matches.push(card.description);
    }

    // 3. Keyword Search (Spheres, Gods, Elements)
    // Optimization: limit to top 3 matches to avoid context flooding
    let count = 0;
    for (let i = 0; i < SPHERES.length; i++) {
        if (count >= 3) break;
        
        // Match Sphere Name (e.g., "Physics")
        if (SPHERES[i].toLowerCase().includes(q)) {
            const card = getSphereCard(i);
            if (card) {
                 matches.push(card.description);
                 count++;
                 continue; // Don't add same card twice
            }
        }
        
        // Match God Name (e.g., "Zeus")
        if (GODS[i].toLowerCase().includes(q)) {
            const card = getSphereCard(i);
            if (card) {
                matches.push(card.description);
                count++;
                continue;
            }
        }

        // Match Element (e.g., "Hydrogen")
        // Be careful with short element names like "Tin", ensure boundary or robust match
        if (ELEMENTS[i].toLowerCase().includes(q) && q.length > 2) {
             const card = getSphereCard(i);
             if (card) {
                 matches.push(card.description);
                 count++;
             }
        }
    }

    if (matches.length === 0) return "";
    
    return "MATCHING ENTRIES FROM GROKBRAIN (PROJECTS & 144 SPHERES ARCHIVE):\n" + matches.join('\n\n');
};
